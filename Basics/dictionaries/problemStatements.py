### 2. **Merge Two Dictionaries**

#Problem Statement: 
# Write a function `mergeDicts(dict1, dict2)` that takes two dictionaries `dict1` and `dict2` and returns a new dictionary that merges both. If the same key exists in both dictionaries, sum their values.



# Example 1
# Input: dict1 = {'a': 1, 'b': 2}, dict2 = {'b': 3, 'c': 4}
# Output: {'a': 1, 'b': 5, 'c': 4}

# Example 2
# Input: dict1 = {'x': 10}, dict2 = {'y': 5, 'x': 2}
# Output: {'x': 12, 'y': 5}




### 3. **Get Values by Keys**

#Problem Statement: 
# Write a function `getValuesByKeys(d, keys)` that takes a dictionary `d` and a list of keys `keys`. Return a list of values corresponding to those keys in the dictionary. If a key does not exist, return `None` for that key.



# Example 1
# Input: d = {'a': 1, 'b': 2, 'c': 3}, keys = ['a', 'c', 'd']
# Output: [1, 3, None]

# Example 2
# Input: d = {'name': 'Alice', 'age': 25}, keys = ['name', 'gender']
# Output: ['Alice', None]




### 4. **Inverting a Dictionary**

#Problem Statement: 
# Write a function `invertDict(d)` that takes a dictionary `d` where all values are unique and returns a new dictionary that inverts the keys and values.



# Example 1
# Input: d = {'a': 1, 'b': 2, 'c': 3}
