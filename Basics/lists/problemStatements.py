### 1. **Find the Maximum Number in a List**

#Problem Statement:  
# Write a function `findMax(lst)` that takes a list of integers `lst` and returns the maximum number in the list. If the list is empty, return `None`.


# Example 1
# Input: lst = [1, 2, 3, 4, 5]
# Output: 5

# Example 2
# Input: lst = [-10, -20, -3]
# Output: -3

# Example 3
# Input: lst = []
# Output: None




### 2. **Remove Duplicates from a List**

#Problem Statement:  
# Write a function `removeDuplicates(lst)` that takes a list `lst` and removes all duplicate elements, returning a new list with unique elements in the order they first appeared.


# Example 1
# Input: lst = [1, 2, 2, 3, 4, 4, 5]
# Output: [1, 2, 3, 4, 5]

# Example 2
# Input: lst = ['a', 'b', 'a', 'c']
# Output: ['a', 'b', 'c']




### 3. **Count Occurrences of an Element**

#Problem Statement:  
# Write a function `countOccurrences(lst, element)` that takes a list `lst` and an element, returning the number of times the element appears in the list.


# Example 1
# Input: lst = [1, 2, 3, 1, 1, 4], element = 1
# Output: 3

# Example 2
# Input: lst = ['a', 'b', 'c', 'a', 'b', 'a'], element = 'b'
# Output: 2




### 4. **Flatten a Nested List**

#Problem Statement:  
# Write a function `flatten(lst)` that takes a nested list and returns a flat list containing all the elements. Assume the list can have arbitrary nesting.


# Example 1
# Input: lst = [1, [2, 3], [4, [5, 6]]]
# Output: [1, 2, 3, 4, 5, 6]

# Example 2
# Input: lst = [[1, 2], [3, 4], 5]
# Output: [1, 2, 3, 4, 5]




### 5. **Rotate a List**

#Problem Statement:  
# Write a function `rotateList(lst, k)` that takes a list `lst` and an integer `k`, and rotates the list to the right by `k` positions. Note that `k` can be larger than the length of the list.


# Example 1
# Input: lst = [1, 2, 3, 4, 5], k = 2
# Output: [4, 5, 1, 2, 3]

# Example 2
# Input: lst = [1, 2, 3, 4, 5], k = 7
